while True:
    print('======================= MADE BY BIANCHI =======================')
    print('Bem-vindo a calculadora de equação de 2º grau!')
    a= float(input('Digita o coeficiente de a= '))
    b= float(input('Digita o coeficiente de b= '))
    c= float(input('Digita o coeficiente de c= '))
    delta = b**2-4*a*c 
    print('delta = ', delta)
    raizdelta=delta**0.5
    print('raiz de delta = ', raizdelta)
    x1=(-b+raizdelta)/(2*a)
    print('x1 = ', x1)
    x2=(-b-raizdelta)/(2*a)
    print('x2 = ', x2)
    continue

